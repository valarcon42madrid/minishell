#include "libft.h"

void	*ft_memset(void *b, int c, size_t len)
{
	size_t	cont;
	char	*a;

	cont = 0;
	a = (char *)b;
	while (len--)
	{
		*a = c;
		a++;
	}
	return (b);
}
